/* ---------------------------------------------------------------------------------------------- */
/*                                            REQUIRES                                            */
/* ---------------------------------------------------------------------------------------------- */
/* ------------------------------------------- Express ------------------------------------------ */
const express = require('express')                              // Express HTTP/S Server
/* -------------------------------------------- Auth -------------------------------------------- */
const auth = require('../../../../utils/auth')                  // Auth module
/* ---------------------------------------------------------------------------------------------- */
/*                                         INITIALIZATIONS                                        */
/* ---------------------------------------------------------------------------------------------- */
/* ------------------------------------------- Express ------------------------------------------ */
const router = express.Router()

/* ---------------------------------------------------------------------------------------------- */
/*                                             ROUTES                                             */
/* ---------------------------------------------------------------------------------------------- */
router.route('/save/editor/:course/:chapter/:lesson')
    .get(auth.isAuth, require('./saveProgress').GET)
    .post(auth.isAuth, require('./saveProgress').POST)

router.route('/complete/:course/:chapter/:lesson')
    .post(auth.isAuth, require('./saveComplete'))

/* ---------------------------------------------------------------------------------------------- */
/*                                             EXPORTS                                            */
/* ---------------------------------------------------------------------------------------------- */
module.exports = router