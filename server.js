const app = express()
app.set('view engine', 'ejs')
app.use('/static', express.static('static')) // CSS, JS, HTML (NOT EJS), images, etc.


/* ---------------------------------------------------------------------------------------------- */
/*                                            CONSTANTS                                           */
/* ---------------------------------------------------------------------------------------------- */
const port = 3080

/* ---------------------------------------------------------------------------------------------- */
/*                                             SERVER                                             */
/* ---------------------------------------------------------------------------------------------- */
app.get('/', async (req, res) => {
    res.render('index')
})

app.listen(process.env.PORT || port)
